const OpenAI = require("openai").OpenAI;
const { GoogleGenerativeAI } = require("@google/generative-ai");
const config = require("../config/config");

const openai = new OpenAI({
  apiKey: config.openai.apiKey,
});

const genAI = new GoogleGenerativeAI(config.gemini.apiKey, { apiVersion: "v1beta" });

const generateOpenAIResponse = async (prompt) => {
  try {
    const response = await openai.chat.completions.create({
      model: "gpt-4o-mini",
      messages: [{ role: "user", content: prompt }],
    });
    return response.choices[0].message.content;
  } catch (error) {
    if (error.status === 429) {
      throw new Error("OpenAI quota exceeded. Please check your plan or try another model.");
    }
    throw error;
  }
};

const generateGeminiResponse = async (prompt) => {
  try {
    const model = genAI.getGenerativeModel(
      { model: "gemini-2.0-flash" },
      { apiVersion: "v1beta" }
    );
    const result = await model.generateContent([{ text: prompt }]);
    const response = await result.response;
    return response.text();
  } catch (error) {
    console.error("Gemini Error Details:", error);
    if (error.status === 429) {
      throw new Error("AI Quota Exceeded. Please try again in 1 minute or switch to another provider.");
    }
    throw error;
  }
};

const generateGroqResponse = async (prompt) => {
  if (!config.groq.apiKey) {
    throw new Error("Groq API Key is missing. Please add it to your .env file.");
  }
  
  const groq = new OpenAI({
    apiKey: config.groq.apiKey,
    baseURL: "https://api.groq.com/openai/v1",
  });

  const response = await groq.chat.completions.create({
    model: "llama-3.3-70b-versatile",
    messages: [
      { role: "system", content: "You are a professional product advisor. Provide concise, expert advice on any product or query provided." },
      { role: "user", content: prompt }
    ],
  });
  return response.choices[0].message.content;
};

const generateDemoResponse = async (prompt) => {
  await new Promise(resolve => setTimeout(resolve, 1500));
  
  const categories = {
    phone: "Smartphone",
    laptop: "Laptop/Computer",
    camera: "Photography Equipment",
    watch: "Wearable Tech",
    shoe: "Footwear",
    budget: "Value-focused",
    best: "Premium Choice"
  };

  let category = "General Product";
  for (const [key, val] of Object.entries(categories)) {
    if (prompt.toLowerCase().includes(key)) {
      category = val;
      break;
    }
  }

  return `### 🌟 Smart Advisor Analysis (${category})

Based on your query: "${prompt.slice(0, 30)}...", here is our expert take:

#### 1. Core Verdict
This product category is currently in a high-innovation cycle. If you are looking for **performance**, focus on the latest generation chips. If you want **value**, last year's flagship models are currently at their best price-to-performance ratio.

#### 2. Key Considerations
*   **Target Audience:** Perfect for users who prioritize reliability over experimental features.
*   **Pros:** High build quality, strong ecosystem support, and excellent resale value.
*   **Cons:** Premium pricing and slight learning curve for advanced settings.

#### 3. Recommendation
**Buy Now** if you need it for immediate work or lifestyle improvement. **Wait** if you can hold on for another 2-3 months as new model announcements are expected soon.

*Note: This is a Demo Response generated by the local expert engine.*`;
};

module.exports = {
  generateOpenAIResponse,
  generateGeminiResponse,
  generateGroqResponse,
  generateDemoResponse,
};
